# To use print function, we can simply call it with the message we want to display.

print("Hello World!")

# for printfing in a new line use \n.
print("\n")
print("This is a Python program\nI am learning Python\nThis is the end of the program")

# for printing with spaces, use \t.
print("\n")
print("\tThis is the first line")
print("\tThis is the second line")

# The print function displays the given message on the console or terminal. In this case, it displayed "Hello World!" on the console.

