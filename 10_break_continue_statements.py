# Break statements

for i in range(12):
     if i == 10:
        break
     print("5 X ",i+1 ," = ", 5*(i+1))

print("\n")
print("================================")
# continue statements

for i in range(16):
     if i == 10:
        print("skiping the iteration")
        continue
     print("5 X ",i+1 ," = ", 5*(i+1))
